
CREATE DATABASE UOADB; 
USE UOADB;
CREATE TABLE Next_of_kin(Kin_id int(11) auto_increment primary key,
						Full_name varchar (30) not null,
						Sex char(1) not null,
						phone_number int (11) not null,
						physical_address varchar(100) not null,
						Email varchar(50) null);

CREATE TABLE Contact_information(contact_id int(11) auto_increment primary key,
						city varchar(30) not null,
						phone_number int(10) not null,
						email varchar(35) not null,
						postal_address varchar(70) null);
						
CREATE TABLE Applicant(applicant_id int(11) auto_increment primary key,
                      First_name varchar(25) not null,
					  Middle_name varchar(25) null,
					  Last_name varchar(25) not null,
                      sex char(1) not null,
					  dob date not null,
					  Nationality varchar(10) not null,
					  Sponsorship varchar(8) not null,
					  contact_id int(11) not null,
					  Kin_id int(11) not null,
					  FOREIGN KEY(Contact_id) REFERENCES Contact_information(contact_id) ON DELETE CASCADE ON UPDATE CASCADE,
					  FOREIGN KEY(Kin_id) REFERENCES Next_of_kin(Kin_id) ON DELETE CASCADE ON UPDATE CASCADE);
					  
CREATE TABLE Disability_information (disability_id int(11) auto_increment primary key,
						Vision varchar(3) not null,
						Hearing varchar(3) not null,
						Phisical_mobility varchar(3) not null,
						Speech varchar (3) not null,
						spinal_cord varchar (3) not null,
						Other varchar(20) not null,
						applicant_id int(11) not null,
						FOREIGN KEY(applicant_id) REFERENCES Applicant(applicant_id) ON DELETE CASCADE ON UPDATE CASCADE);
					  
CREATE TABLE Education_information(Edu_id int(11) auto_increment primary key,
						Form_four_index varchar(15) not null,
						Entry_category varchar(10) not null,
						Avn_or_Acse_no varchar (15) not null,
						applicant_id int(11) not null,
						FOREIGN KEY(applicant_id) REFERENCES Applicant(applicant_id) ON DELETE CASCADE ON UPDATE CASCADE);

CREATE TABLE Payment(Receipt_id int(11) auto_increment primary key,
	                Receipt_no varchar (15) NOT NULL,
					Date_of_payment timestamp not null,
					Amount_paid varchar (6) not null,
					Applicant_id int(11) not null,
					FOREIGN KEY(applicant_id) REFERENCES Applicant(applicant_id) ON DELETE CASCADE ON UPDATE CASCADE);

CREATE TABLE Programme(programme_id int(10) auto_increment primary key,
						proramme_name varchar(10)not null,
						programme_type varchar (10) not null,
						programme_requirement varchar(250) not null,
						programme_duration varchar(10) not null);

CREATE TABLE Application_status(status_id int(10)auto_increment primary key,
						status varchar(10) not null,
						programme_id int(10) not null,
						Applicant_id int(11) not null,
						FOREIGN KEY(programme_id) REFERENCES Programme(programme_id) ON DELETE CASCADE ON UPDATE CASCADE,
						FOREIGN KEY(applicant_id) REFERENCES Applicant(applicant_id) ON DELETE CASCADE ON UPDATE CASCADE);

